    public Map<String, Object> createTenant(Map<String, Object> request, String actorUserId) {
        Map<String, Object> payload = normalizeTenantPayload(request, true);
        if (StringUtils.hasText(stringValue(payload.get("tenantId")))) {
            throw new BusinessException("tenantId is system-generated and must not be provided", "TENANT_ID_AUTOGENERATED");
        }

        String companyCode = stringValue(payload.get("companyCode"));
        if (!StringUtils.hasText(companyCode)) {
            throw new BusinessException("companyCode is required", "COMPANY_CODE_REQUIRED");
        }
        ensureUniqueFieldValue(TENANTS_COLLECTION, "companyCode", companyCode, null, null);

        String domain = stringValue(payload.get("domain"));
        if (StringUtils.hasText(domain)) {
            ensureUniqueFieldValue(TENANTS_COLLECTION, "domain", domain, null, null);
        }

        Map<String, Object> created = createResource(TENANTS_COLLECTION, payload, "tenantId", "TNT-", 4, null, Map.of(
            "isActive", true));
        publishAudit(actorUserId, "TENANT_CREATED", "MDM_TENANT", stringValue(created.get("tenantId")),
            metadataOf("companyCode", stringValue(created.get("companyCode"))));
        return created;
    }

