    private Map<String, Object> createResource(
            String collectionName,
            Map<String, Object> request,
            String idField,
            String prefix,
            int width,
            Map<String, String> fallbackFields,
            Map<String, Object> defaults) {
        Map<String, Object> payload = new LinkedHashMap<>(request == null ? Map.of() : request);
        payload.put(idField, businessIdGeneratorService.nextId(collectionName, idField, prefix, width));

        if (fallbackFields != null) {
            fallbackFields.forEach((field, sourceField) -> {
                if (!StringUtils.hasText(stringValue(payload.get(field)))) {
                    payload.put(field, payload.get(sourceField));
                }
            });
        }

        String businessId = stringValue(payload.get(idField));
        if (exists(collectionName, idField, businessId)) {
            throw new BusinessException(idField + " already exists: " + businessId, "DUPLICATE_RESOURCE");
        }

        defaults.forEach(payload::putIfAbsent);
        payload.put("createdAt", Date.from(Instant.now()));
        payload.put("updatedAt", Date.from(Instant.now()));
        Document saved = mongoTemplate.insert(new Document(payload), collectionName);
        return toMap(saved);
    }

    private List<Map<String, Object>> listResources(String collectionName, Sort sort, Boolean isActive) {
        Query query;
        if (isActive == null) {
            query = new Query(Criteria.where("isActive").is(true));
        } else {
            query = new Query(Criteria.where("isActive").is(isActive));
        }
        query.with(sort);
        return mongoTemplate.find(query, Document.class, collectionName).stream().map(this::toMap).toList();
    }

    private Map<String, Object> getResource(String collectionName, String idField, String idValue) {
        Document document = findActive(collectionName, idField, idValue);
        return toMap(document);
    }

    private Map<String, Object> updateResource(String collectionName, String idField, String idValue, Map<String, Object> request) {
        Document existing = findActive(collectionName, idField, idValue);
        if (request != null) {
            request.forEach((key, value) -> {
                if (!"_id".equals(key) && !idField.equals(key)) {
                    existing.put(key, value);
                }
            });
        }
        existing.put("updatedAt", Date.from(Instant.now()));
        return toMap(mongoTemplate.save(existing, collectionName));
    }

    private void deleteResource(String collectionName, String idField, String idValue) {
        Document existing = findActive(collectionName, idField, idValue);
        if (existing.containsKey("isActive")) {
            existing.put("isActive", false);
        }
        existing.put("updatedAt", Date.from(Instant.now()));
        mongoTemplate.save(existing, collectionName);
    }

    private Map<String, Object> reactivateResource(String collectionName,
                                                   String idField,
                                                   String idValue,
                                                   boolean includeStatus) {
        Query query = new Query(Criteria.where(idField).is(idValue));
        Document existing = mongoTemplate.findOne(query, Document.class, collectionName);
        if (existing == null) {
            throw new ResourceNotFoundException("Resource", idValue);
        }

        existing.put("isActive", true);
        if (includeStatus) {
            existing.put("status", "Active");
        }
        existing.put("updatedAt", Date.from(Instant.now()));
        return toMap(mongoTemplate.save(existing, collectionName));
    }

    private boolean exists(String collectionName, String idField, String idValue) {
        Query query = new Query(Criteria.where(idField).is(idValue));
        return mongoTemplate.exists(query, collectionName);
    }

    private void ensureUniqueFieldValue(String collectionName,
                                        String uniqueField,
                                        String uniqueValue,
                                        String idField,
                                        String idValue) {
        if (!StringUtils.hasText(uniqueValue)) {
            return;
        }
        Criteria criteria = Criteria.where(uniqueField).is(uniqueValue);
        if (StringUtils.hasText(idField) && StringUtils.hasText(idValue)) {
            criteria = criteria.and(idField).ne(idValue);
        }
        if (mongoTemplate.exists(new Query(criteria), collectionName)) {
            throw new BusinessException(uniqueField + " already exists: " + uniqueValue, "DUPLICATE_RESOURCE");
        }
    }

