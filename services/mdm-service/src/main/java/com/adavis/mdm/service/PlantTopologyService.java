    public Map<String, Object> updateTenant(String tenantId, Map<String, Object> request, String actorUserId) {
        Document existing = findActive(TENANTS_COLLECTION, "tenantId", tenantId);
        Map<String, Object> payload = normalizeTenantPayload(request, false);

        String incomingCompanyCode = stringValue(payload.get("companyCode"));
        String currentCompanyCode = stringValue(existing.get("companyCode"));
        if (StringUtils.hasText(incomingCompanyCode) && !equalsIgnoreCaseTrimmed(incomingCompanyCode, currentCompanyCode)) {
            ensureUniqueFieldValue(TENANTS_COLLECTION, "companyCode", incomingCompanyCode, "tenantId", tenantId);
        }

        String incomingDomain = stringValue(payload.get("domain"));
        String currentDomain = stringValue(existing.get("domain"));
        if (StringUtils.hasText(incomingDomain) && !equalsIgnoreCaseTrimmed(incomingDomain, currentDomain)) {
            ensureUniqueFieldValue(TENANTS_COLLECTION, "domain", incomingDomain, "tenantId", tenantId);
        }

        Map<String, Object> updated = updateResource(TENANTS_COLLECTION, "tenantId", tenantId, payload);
        publishAudit(actorUserId, "TENANT_UPDATED", "MDM_TENANT", tenantId,
            metadataOf("companyCode", stringValue(updated.get("companyCode"))));
        return updated;
    }

