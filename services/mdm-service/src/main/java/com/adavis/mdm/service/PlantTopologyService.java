    private Map<String, Object> markInactive(Map<String, Object> resource) {
        Map<String, Object> after = new LinkedHashMap<>(resource == null ? Map.of() : resource);
        after.put("isActive", false);
        after.put("updatedAt", Instant.now().toString());
        return after;
    }

    private Map<String, Object> resourceMetadata(Map<String, Object> resource, String codeField) {
        Map<String, Object> metadata = new LinkedHashMap<>();
        if (resource == null) {
            return metadata;
        }

        String tenantId = stringValue(resource.get("tenantId"));
        if (StringUtils.hasText(tenantId)) {
            metadata.put("tenantId", tenantId);
        }

        String codeValue = stringValue(resource.get(codeField));
        if (StringUtils.hasText(codeValue)) {
            metadata.put(codeField, codeValue);
        }
        return metadata;
    }

    private Map<String, Object> normalizeTenantPayload(Map<String, Object> request, boolean isCreate) {
        Map<String, Object> payload = new LinkedHashMap<>(request == null ? Map.of() : request);
        payload.remove("status");

        normalizeTrimmedField(payload, "companyCode");
        normalizeTrimmedField(payload, "domain");

        if (isCreate) {
            payload.putIfAbsent("isActive", true);
            return payload;
        }
        return payload;
    }

    private Map<String, Object> normalizeTopologyPayload(Map<String, Object> request) {
        Map<String, Object> payload = new LinkedHashMap<>(request == null ? Map.of() : request);
        payload.remove("status");
        return payload;
    }

    private Map<String, Object> normalizeAssignmentPayload(Map<String, Object> request) {
        Map<String, Object> payload = new LinkedHashMap<>(request == null ? Map.of() : request);

        normalizeTrimmedField(payload, "plantId");
        normalizeTrimmedField(payload, "departmentId");
        normalizeTrimmedField(payload, "resourceType");
        normalizeTrimmedField(payload, "resourceId");

        String plantId = stringValue(payload.get("plantId"));
        String resourceType = stringValue(payload.get("resourceType"));
        String resourceId = stringValue(payload.get("resourceId"));

        if (!StringUtils.hasText(plantId) && "PLANT".equalsIgnoreCase(resourceType) && StringUtils.hasText(resourceId)) {
            plantId = resourceId;
            payload.put("plantId", plantId);
        }

        requireTextField(plantId, "plantId", "PLANT_ID_REQUIRED");

        if (!StringUtils.hasText(stringValue(payload.get("departmentId")))) {
            payload.remove("departmentId");
        }

        return payload;
    }

    private Document findActive(String collectionName, String idField, String idValue) {
        Query query = new Query(Criteria.where(idField).is(idValue).and("isActive").is(true));
        Document document = mongoTemplate.findOne(query, Document.class, collectionName);
        if (document == null) {
            throw new ResourceNotFoundException("Resource", idValue);
        }
        return document;
    }

    private String stringValue(Object value) {
        return value == null ? null : String.valueOf(value);
    }

    private void normalizeTrimmedField(Map<String, Object> payload, String field) {
        if (payload == null || !payload.containsKey(field)) {
            return;
        }
        String value = stringValue(payload.get(field));
        if (value == null) {
            payload.remove(field);
            return;
        }
        String trimmed = value.trim();
        if (trimmed.isEmpty()) {
            payload.remove(field);
            return;
        }
        payload.put(field, trimmed);
    }

    private void requireTextField(String value, String fieldName, String errorCode) {
        if (!StringUtils.hasText(value)) {
            throw new BusinessException(fieldName + " is required", errorCode);
        }
    }

    private boolean equalsIgnoreCaseTrimmed(String a, String b) {
        if (!StringUtils.hasText(a) && !StringUtils.hasText(b)) {
            return true;
        }
        if (!StringUtils.hasText(a) || !StringUtils.hasText(b)) {
            return false;
        }
        return a.trim().equalsIgnoreCase(b.trim());
    }

    private Map<String, Object> toMap(Document document) {
        Map<String, Object> result = new LinkedHashMap<>();
        document.forEach((key, value) -> {
            if (!RESPONSE_EXCLUDED_FIELDS.contains(key)) {
                result.put(key, normalizeValue(value));
            }
        });
        return result;
    }

    private Object normalizeValue(Object value) {
        if (value instanceof Date date) {
            return date.toInstant().toString();
        }
        return value;
    }
